package com.hash.p79.domain;

public class HashTable {
	
	private Long id;
	private String clientnbr;
	private String hashmessage;
	private String disguise;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getClientnbr() {
		return clientnbr;
	}
	public void setClientnbr(String clientnbr) {
		this.clientnbr = clientnbr;
	}
	public String getHashmessage() {
		return hashmessage;
	}
	public void setHashmessage(String hashmessage) {
		this.hashmessage = hashmessage;
	}
	public String getDisguise() {
		return disguise;
	}
	public void setDisguise(String disguise) {
		this.disguise = disguise;
	}
	
	

}
